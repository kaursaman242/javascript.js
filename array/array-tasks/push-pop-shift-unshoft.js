// Create an array fruits = ["apple", "banana", "cherry"].
// Add "orange" at the end using push().
// Add "mango" at the beginning using unshift().
// Remove the last item using pop().
// Remove the first item using shift().
// Print the array after each operation.
const fruits = ["apple", "banana", "cherry"];

fruits.push("orange");
fruits.unshift("mango");
fruits.pop();
fruits.shift();
console.log(fruits);